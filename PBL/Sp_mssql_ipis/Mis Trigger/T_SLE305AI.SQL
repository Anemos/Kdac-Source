CREATE TRIGGER PBIPIS.T_SLE305AI
   AFTER INSERT ON PBSLE.SLE305
   REFERENCING NEW ROW AS NR
   FOR EACH ROW
   MODE DB2SQL
   WHEN
    (NR.GUBUN = '5')
BEGIN
  INSERT INTO PBIPIS.PDSLE305 (CHGDATE, CHGCD,   SRNO, GUBUN,
                               COMMENT, STSCD,   DOWNDATE)
    VALUES (CHAR(CURRENT TIMESTAMP), 'A', NR.SRNO,
                 NR.GUBUN, NR.COMMENT,
                   'N', ' ') ;
END


