CREATE TRIGGER PBIPIS.T_INV002AI
   AFTER INSERT ON PBINV.INV002
   REFERENCING NEW ROW AS NR
   FOR EACH ROW
   MODE DB2SQL
BEGIN
  INSERT INTO PBIPIS.PDINV002 (CHGDATE, CHGCD, ITNO, ITNM, SPEC, XUNIT,
                               XTYPE, STSCD, DOWNDATE, RVNO)
    VALUES (CHAR(CURRENT TIMESTAMP), 'A', NR.ITNO, NR.ITNM, NR.SPEC,
                               NR.XUNIT, NR.XTYPE, 'N', ' ', NR.RVNO);
END
