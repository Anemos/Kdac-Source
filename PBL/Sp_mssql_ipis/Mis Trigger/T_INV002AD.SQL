CREATE TRIGGER PBIPIS.T_INV002AD
   AFTER DELETE ON PBINV.INV002
   REFERENCING OLD ROW AS OR
   FOR EACH ROW
   MODE DB2SQL
BEGIN
  INSERT INTO PBIPIS.PDINV002 (CHGDATE, CHGCD, ITNO, ITNM, SPEC, XUNIT,
                               XTYPE, STSCD, DOWNDATE, RVNO)
    VALUES (CHAR(CURRENT TIMESTAMP), 'D', OR.ITNO, OR.ITNM, OR.SPEC,
                               OR.XUNIT, OR.XTYPE, 'N', ' ', OR.RVNO);
END
