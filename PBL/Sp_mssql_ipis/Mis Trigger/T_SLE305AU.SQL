CREATE TRIGGER PBIPIS.T_SLE305AU
   AFTER UPDATE ON PBSLE.SLE305
   REFERENCING OLD ROW AS OR
               NEW ROW AS NR
   FOR EACH ROW
   MODE DB2SQL
   WHEN
    (NR.GUBUN = '5' AND OR.COMMENT <> NR.COMMENT)
BEGIN
  INSERT INTO PBIPIS.PDSLE305 (CHGDATE, CHGCD, SRNO, GUBUN,
                               COMMENT, STSCD,   DOWNDATE)
    VALUES (CHAR(CURRENT TIMESTAMP), 'R',
                 NR.SRNO, NR.GUBUN, NR.COMMENT,
                   'N', ' ') ;
END


