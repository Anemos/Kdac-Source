CREATE TRIGGER PBIPIS.T_SLE305AD
   AFTER DELETE ON PBSLE.SLE305
   REFERENCING OLD ROW AS OR
   FOR EACH ROW
   MODE DB2SQL
   WHEN
    (OR.GUBUN = '5')
BEGIN
  INSERT INTO PBIPIS.PDSLE305 (CHGDATE, CHGCD,  SRNO, GUBUN,  COMMENT,
                               STSCD,   DOWNDATE)
    VALUES (CHAR(CURRENT TIMESTAMP), 'D',
               OR.SRNO, OR.GUBUN, OR.COMMENT,
                   'N', ' ') ;
END


