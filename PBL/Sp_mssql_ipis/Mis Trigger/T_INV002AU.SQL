CREATE TRIGGER PBIPIS.T_INV002AU
   AFTER UPDATE OF ITNO, ITNM, SPEC, XUNIT, XTYPE ON PBINV.INV002
   REFERENCING NEW ROW AS NR
               OLD ROW AS OR
   FOR EACH ROW
   MODE DB2SQL
   WHEN
      (NR.ITNO  <> OR.ITNO OR NR.ITNM <> OR.ITNM OR
       NR.SPEC  <> OR.SPEC OR NR.XUNIT <> OR.XUNIT OR
       NR.XTYPE <> OR.XTYPE OR NR.RVNO <> OR.RVNO)
BEGIN
  INSERT INTO PBIPIS.PDINV002 (CHGDATE, CHGCD, ITNO, ITNM, SPEC, XUNIT,
                               XTYPE, STSCD, DOWNDATE, RVNO)
    VALUES (CHAR(CURRENT TIMESTAMP), 'R', NR.ITNO, NR.ITNM, NR.SPEC,
                               NR.XUNIT, NR.XTYPE, 'N', ' ', NR.RVNO);
END
